    public void showItemToFields(int index){
        jTextField_order_id.setText(Integer.toString(retrieveData().get(index).getId()));
        String orderStatus = retrieveData().get(index).getOrderStatus();
        jTextField_order_status.setText(orderStatus);
        if("processed".equals(orderStatus)){
            jButton_update_order_status.disable();
        }
    }
    
