    private void jButton_update_order_statusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_update_order_statusActionPerformed
        // TODO add your handling code here:
        if (jTextField_order_id!=null || jTextField_order_status!=null) {
            String orderStatus = jTextField_order_status.getText();
            if(!"processing".equals(orderStatus) || !"processed".equals(orderStatus)){
                JOptionPane.showMessageDialog(null,"The order status must be processing or processed");
            }
            String qry=null;
            PreparedStatement ps=null;
            Connection conn = mysql.getConnection();

            try {
                qry="update orders set status=? where id=?";
                ps=conn.prepareStatement(qry);

                ps.setString(1,orderStatus);
                ps.setInt(2, Integer.parseInt(jTextField_order_id.getText()));

                int res=ps.executeUpdate();
                fillTable();
                if (res>=1) {
                    JOptionPane.showMessageDialog(null, res+" order"
                        + " Updated into database .....");
                }else
                JOptionPane.showMessageDialog(null, "Order Updation Failed ....");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
            jTextField_order_id.setText("");
            jTextField_order_status.setText("");

        }else{
          JOptionPane.showMessageDialog(null, "All fields are mandatory.......");
        }
    }//GEN-LAST:event_jButton_update_order_statusActionPerformed

